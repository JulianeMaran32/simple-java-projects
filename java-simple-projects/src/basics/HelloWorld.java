package basics; // Define o pacote (pasta lógica) onde esta classe se encontra.

// Todo código Java vive dentro de classes.
// O nome do arquivo .java deve ser exatamente o mesmo nome da classe pública principal.
public class HelloWorld { // A classe principal do nosso programa simples.

    // Este é o método principal (main).
    // É o ponto de partida da execução do programa pela Máquina Virtual Java (JVM).
    public static void main(String[] args) {
        // Imprime uma mensagem na tela (no console).
        System.out.println("Olá, Mundo!");

        // Imprime outra mensagem na tela.
        System.out.println("Meu primeiro programa Java!");

        // O programa termina automaticamente ao sair deste método.
    }

}